class Solution:
    
    # arr[] : the input array
    # N : size of the array arr[]
    
    #Function to return length of longest subsequence of consecutive integers.
    def findLongestConseqSubseq(self,arr, N):
        n = set(arr)
        length = 0
        for i in arr:
            if i-1 not in n:
                currentNum = i
                currentLen = 1
                while (currentNum+1) in n:  #find all nums of a consecutive sequence
                    currentLen+=1
                    currentNum+=1
                length = max(length, currentLen)
        return length
                

